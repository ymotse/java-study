package estudos;

public class MaiusculoMinusculo {

	public static void main(String[] args) {
		
		String frase = "o RaTo RoEu A rOuPa Do ReI dE rOmA";
		
		System.out.println(frase); // do jeito que se encontra;
		
		System.out.println(frase.toLowerCase()); // tudo em minusculo;
		
		System.out.println(frase.toUpperCase()); // tudo em maiusculo;
		
	}

}
